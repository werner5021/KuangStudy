package com.werner.base;

public class Demo02 {

	public static void main(String[] args) {
		// 八大基本數據類型
		
		//整數
		int mun1 = 10;  //最常用
		byte num2 = 20;
		short num3 = 30;
		long num4 = 30L;  //Long 要在數字後加L
				
		//小數
		float num5 = 50.1F;  //float 要在數字後加F
		double num6 = 3.1415926111111111;
	
		//字符
		char name = '李';
		// 字串，String 不是關鍵詞，是類
		String name1 = "知恩";
		
		//boolean
		boolean flag = true;
		boolean flag1 = false;
	}

}
